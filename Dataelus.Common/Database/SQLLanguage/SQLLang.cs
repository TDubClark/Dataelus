		public static List<string> EnbracketSQLServer (IEnumerable<string> fields)
		{
			return Enbracket (fields, SQLLanguageImplementation.MSSQLServer);
		}

		public static string EnbracketSQLServer (string field)
		{
			return Enbracket (field, true, SQLLanguageImplementation.MSSQLServer);
		}

		public static List<string> Enbracket (IEnumerable<string> fields, SQLLanguageImplementation implementation)
		{
			const bool removeBrackets = true;
			return fields.Select (x => Enbracket (x, removeBrackets, implementation)).ToList ();
		}

		public static string Enbracket (string field)
		{
			const bool removeBrackets = true;
			return Enbracket (field, removeBrackets, SQLLanguageImplementation.GenericEnbracketAll);
		}
		public static string Enbracket (string field, bool removeBrackets, SQLLanguageImplementation implementation)
		{
			var comparer = new StringEqualityComparer ();

			if (removeBrackets) {
				// Check for existing brackets
				var mtc = Regex.Match (field, @"[\[\]]");
				if (mtc.Success) {
					// Remove any existing brackets
					field = Regex.Replace (field, @"[\[\]]", "");
				}
			}

			if (removeBrackets || !Regex.IsMatch (field, @"^\[.*\]$")) {  // Check for existing brackets
				if (Regex.IsMatch (field, @"[^A-Za-z0-9_]") || IsReservedWord (field, comparer, implementation)) {
					return string.Format ("[{0}]", Regex.Replace (field, @"\[|\]", ""));
				}
			}
			return field;
		}

		public static bool IsReservedWordSQLServer (string word, IEqualityComparer<string> comparer)
		{
			return GetKeyWordsSQLServer ().Contains (word, comparer);
		}

		public static bool IsReservedWordODBC (string word, IEqualityComparer<string> comparer)
		{
			return GetKeyWordsODBCReserved ().Contains (word, comparer);
		}

		public static bool IsReservedWordMySQL (string word, IEqualityComparer<string> comparer)
		{
			return GetKeyWordsMySQL5_5 ().Contains (word, comparer);
		}

		public static bool IsReservedWordOracle (string word, IEqualityComparer<string> comparer)
		{
			var words = GetReservedWordsOracleAll ();
			return words.Contains (word, comparer);
		}

		public static bool IsReservedWord (string word, IEqualityComparer<string> comparer, SQLLanguageImplementation implementation)
		{
			if (implementation == SQLLanguageImplementation.GenericEnbracketAll)
				return true;
			return GetKeyWords (implementation).Contains (word, comparer);
		}

		/// <summary>
		/// Gets the key words for the given implementation of the SQL Language.
		/// </summary>
		/// <returns>The key words.</returns>
		/// <param name="implementation">Implementation.</param>
		public static string[] GetKeyWords (SQLLanguageImplementation implementation)
		{
			switch (implementation) {
				case SQLLanguageImplementation.MSAccess:
					return GetKeyWordsMSAccess ();
				case SQLLanguageImplementation.MSSQLServer:
					return GetKeyWordsSQLServer ();
				case SQLLanguageImplementation.MySQL:
					return GetKeyWordsMySQL5_5 ();
				case SQLLanguageImplementation.ODBC:
					return GetKeyWordsODBCReserved ();
				case SQLLanguageImplementation.Oracle:
					return GetReservedWordsOracleAll ().ToArray ();
				case SQLLanguageImplementation.GenericEnbracketAll:
					return new string[]{ };
				default:
					throw new ArgumentException (String.Format ("Invalid SQL Implmentation: '{0}'", implementation), "implementation");
			}
		}


